package ch.bfh.swos.flightplanning.service.dto;

import java.io.Serializable;

/**
 * 
 */
public class PlaneDTO implements Serializable {
	private static final long serialVersionUID = 1L;

	private Long id;
	
	private String icao;
	private String name;
	private int seats;
	
	private AirlineDTO airline;

	public PlaneDTO() {
	}

	public PlaneDTO(String icao, String name, int seats) {
		this.icao = icao;
		this.name = name;
		this.seats = seats;
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getIcao() {
		return icao;
	}

	public void setIcao(String icao) {
		this.icao = icao;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getSeats() {
		return seats;
	}

	public void setSeats(int seats) {
		this.seats = seats;
	}

	public AirlineDTO getAirline() {
		return airline;
	}

	public void setAirline(AirlineDTO airline) {
		this.airline = airline;
	}

}
